def resta(x, y):
    """
    Retorna la resta entre dos números.

    Parámetros:
    x (int, float): Minuendo.
    y (int, float): Sustraendo.

    Retorno:
    int, float: Resultado de la resta.
    """
    return x - y

def suma(x, y, z):
    """
    Retorna la suma de tres números.

    Parámetros:
    x (int, float): Primer número.
    y (int, float): Segundo número.
    z (int, float): Tercer número.

    Retorno:
    int, float: Resultado de la suma.
    """
    return x + y + z

def multiplicacion(x, y):
    """
    Retorna el producto de dos números.

    Parámetros:
    x (int, float): Primer número.
    y (int, float): Segundo número.

    Retorno:
    int, float: Resultado de la multiplicación.
    """
    return x * y