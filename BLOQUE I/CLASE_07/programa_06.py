# Inicialización de la variable
a = 2

# Bucle while: se ejecuta mientras 'a' sea menor que 8
while a < 8:
    print(f"Hola, {a}")  # Muestra el mensaje junto con el valor de 'a'
    a += 1  # Incrementa 'a' en 1 en cada iteración